package EncapsulationConcept;

public class classA {
	
	private String username; 
	private int password;
	
	public String getUsername() {
		return username;
	}
	
	public int getPassword() {
		return password;
	}
	
	public void setUsername(String un) {
		this.username = un;
	}
	
	public void setPassword(int pw) {
		this.password = pw;
	}
}
